-- Create a function to validate template paragraphs configuration
CREATE OR REPLACE FUNCTION validate_template_paragraphs_config(paragraphs JSONB)
RETURNS BOOLEAN AS $$
DECLARE
    paragraph JSONB;
BEGIN
    -- Check if paragraphs is valid JSONB
    IF paragraphs IS NULL THEN
        RETURN true; -- NULL is allowed (no specific paragraphs defined)
    END IF;
    
    -- Check paragraphs is an array
    IF jsonb_typeof(paragraphs) != 'array' THEN
        RETURN false;
    END IF;
    
    -- Validate each paragraph
    FOR paragraph IN SELECT jsonb_array_elements(paragraphs)
    LOOP
        -- Check required paragraph fields
        IF NOT (paragraph ? 'section_name' AND paragraph ? 'section_type' AND paragraph ? 'description') THEN
            RETURN false;
        END IF;
        
        -- Check section type is valid
        IF NOT (paragraph->>'section_type' IN ('header', 'paragraph', 'list', 'table', 'code_block', 'summary', 'conclusion')) THEN
            RETURN false;
        END IF;
    END LOOP;
    
    RETURN true;
END;
$$ LANGUAGE plpgsql;

-- Add check constraint to validate the template paragraphs configuration
ALTER TABLE templates 
ADD CONSTRAINT chk_templates_template_paragraphs 
CHECK (validate_template_paragraphs_config(template_paragraphs));


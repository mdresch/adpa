-- Create a view for templates with all AI enhancements
CREATE OR REPLACE VIEW templates_with_full_ai_enhancements AS
SELECT 
    t.*,
    CASE 
        WHEN t.system_prompt IS NOT NULL AND t.system_prompt != '' THEN true 
        ELSE false 
    END as has_system_prompt,
    CASE 
        WHEN t.context_injection_config->>'enabled' = 'true' THEN true 
        ELSE false 
    END as context_injection_enabled,
    CASE 
        WHEN t.prompt_build_up->>'enabled' = 'true' THEN true 
        ELSE false 
    END as prompt_build_up_enabled,
    CASE 
        WHEN t.template_paragraphs IS NOT NULL AND jsonb_array_length(t.template_paragraphs) > 0 THEN true 
        ELSE false 
    END as has_template_paragraphs,
    jsonb_array_length(t.context_injection_config->'sources') as context_sources_count,
    jsonb_array_length(t.prompt_build_up->'stages') as prompt_stages_count,
    jsonb_array_length(t.template_paragraphs) as template_paragraphs_count
FROM templates t
WHERE t.deleted_at IS NULL;

-- Grant permissions on the view
GRANT SELECT ON templates_with_full_ai_enhancements TO authenticated_users;

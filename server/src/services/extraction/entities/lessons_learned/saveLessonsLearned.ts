/**
 * Save Lessons Learned
 */

import { logger } from '../../../../utils/logger'
import type { PoolClient } from 'pg'
import type { PersistenceResult } from '../../base/Persistence'
import type { LessonsLearned } from './types'

export async function saveLessonsLearned(
    client: PoolClient,
    projectId: string,
    userId: string,
    entities: LessonsLearned[]
): Promise<PersistenceResult> {
    if (entities.length === 0) {
        return { saved: 0, skipped: 0, failed: 0 }
    }

    try {
        // Delete existing records for this project
        await client.query('DELETE FROM lessons_learned WHERE project_id = $1', [projectId])

        const values: any[] = []
        const placeholders: string[] = []

        entities.forEach((e, index) => {
            const offset = index * 9
            placeholders.push(
                `($${offset + 1}, $${offset + 2}, $${offset + 3}, $${offset + 4}, $${offset + 5}, $${offset + 6}, $${offset + 7}, $${offset + 8}, $${offset + 9})`
            )

            values.push(
                projectId,
                e.category || null,
                e.description || '',
                e.situation || null,
                e.outcome || null,
                e.recommendations || null,
                e.positive_or_negative ?? true,
                e.source_document_id || null,
                userId
            )
        })

        await client.query(
            `INSERT INTO lessons_learned (
        project_id, category, description, 
        situation, outcome, recommendations, positive_or_negative,
        source_document_id, created_by
      )
      VALUES ${placeholders.join(', ')}`,
            values
        )

        return {
            saved: entities.length,
            skipped: 0,
            failed: 0
        }
    } catch (error: unknown) {
        logger.error('[EXTRACTION-LESSONS-LEARNED] Failed to save', {
            projectId,
            error: error instanceof Error ? error.message : String(error)
        })
        return {
            saved: 0,
            skipped: 0,
            failed: entities.length,
            error: error instanceof Error ? error.message : String(error)
        }
    }
}
